

int main()
{
    Engine engine {};

    while(window.isOpen())
    {
        sf::Event event;
        while(window.pollEvent(event))
        {
            switch(event.type)
            {
                case sf::Event::Closed:
                    window.close();
                    break;
            }
        }

        engine.simulate();
        engine.render(window);
    }
}